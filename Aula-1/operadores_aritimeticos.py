#Os operadores aritméticos em Python são utilizados para realizar operações matemáticas entre variáveis e valores. 
#Aqui estão os principais operadores aritméticos em Python:

# Adição (+): Soma dois valores.

resultado = 5 + 3  # Resultado será 8



# Subtração (-): Subtrai o valor à direita do valor à esquerda.

resultado = 7 - 4  # Resultado será 3



# Multiplicação (*): Multiplica dois valores.

resultado = 6 * 2  # Resultado será 12



# Divisão (/): Divide o valor à esquerda pelo valor à direita. O resultado é sempre um número de ponto flutuante.

resultado = 10 / 3  # Resultado será aproximadamente 3.3333



# Divisão Inteira (//): Divide o valor à esquerda pelo valor à direita, resultando em um quociente inteiro.

resultado = 10 // 3  # Resultado será 3



# Módulo (%): Retorna o resto da divisão do valor à esquerda pelo valor à direita.

resultado = 10 % 3  # Resultado será 1


# Exponenciação ():**  Eleva o valor à esquerda à potência do valor à direita.

resultado = 2 ** 3  # Resultado será 8
